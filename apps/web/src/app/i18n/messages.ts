export function resolveWordings(
  labels: string[],
  locale: AppLocale,
): Record<string, string> {
  const catalog = MESSAGES[locale] ?? MESSAGES.en;
  const fallback = MESSAGES.en;
  const result: Record<string, string> = {};
  for (const key of labels) {
    if (!key) continue;
    result[key] = catalog[key] ?? fallback[key] ?? key;
  }
  return result;
}

/** Sync lookup for non-Lit strings (dialogs, errors). */
export function tx(key: string, locale: AppLocale = getAppLocale()): string {
  return MESSAGES[locale]?.[key] ?? MESSAGES.en[key] ?? key;
}

/** Sync lookup with `{name}` / `{n}` / `{date}` interpolation. */
export function tf(
  key: string,
  vars: Record<string, string | number>,
  locale?: AppLocale,
): string {
  let text = tx(key, locale);
  for (const [name, value] of Object.entries(vars)) {
    text = text.replaceAll(`{${name}}`, String(value));
  }
  return text;
}
